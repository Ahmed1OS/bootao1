var boddy = document.querySelector(".duddy")
var spin = document.querySelector("#spinners")
var pin = document.querySelector("#pin")




window.setTimeout(swag , 0000)

function swag(){
    pin.style.display = "block"
    spin.style.display = "block"
    boddy.style.display = "none"
}




window.setTimeout(times , 3000)

function times(){
    pin.style.display = "none"
    spin.style.display = "none"
    boddy.style.display = "block"
}